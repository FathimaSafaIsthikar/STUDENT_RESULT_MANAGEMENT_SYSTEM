    private void btn_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signupActionPerformed
        // TODO add your handling code here:
        String fname=txt_fname.getText();
        String lname=txt_lname.getText();
        String password=String.valueOf(txt_pass.getPassword());
        String username=txt_uname.getText();
        String question=txt_quest.getText();
        String answer=txt_ans.getText();
        
        if(username.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Username field is empty");
        }
        else if(password.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Password field is empty");
        }
         else if(fname.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"First Name field is empty");
        }
          else if(lname.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Last Name field is empty");
        }
          else if(question.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Question field is empty");
            
        }else if(answer.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Answer field is empty");
        }
        
        else
        {
            int p=JOptionPane.showConfirmDialog(null,"Are you sure to add this record?","Add Record",JOptionPane.YES_NO_OPTION);
            
if(p==0){
    String sql="INSERT INTO user(uname,pass,fname,lname,quest,ans) VALUES (?,?,?,?,?,?)";

try{
                 
                 ps = conn.prepareStatement(sql);
                 
                 ps.setString(6,answer);
                 ps.setString(5,question);
                 ps.setString(4,lname);
                 ps.setString(3,fname);
                 ps.setString(2,password);
                 ps.setString(1,username);
                 
                 ps.executeUpdate();
                 JOptionPane.showMessageDialog(null,"Data Saved Successfully");   

                 
                 
}
           catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());
            }
}
else{
    JOptionPane.showMessageDialog(null,"Data Not Saved Successfully");
}
    }

        
        
    }//GEN-LAST:event_btn_signupActionPerformed

