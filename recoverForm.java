    private void txt_unameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_unameKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            try {
                String sql="SELECT * FROM `user` WHERE `uname`=?";
                ps=conn.prepareStatement(sql);
                
                ps.setString(1,txt_uname.getText());
                rs=ps.executeQuery();
                if(rs.next()){
                    txt_name.setText(rs.getString("fname"));
                    txt_quest.setText(rs.getString("quest"));
                }
                else{
                    JOptionPane.showMessageDialog(null,"Username not found");
                }
                
            } catch (Exception ex) {
                 JOptionPane.showMessageDialog(null,ex.getMessage());
            }
        }
    }//GEN-LAST:event_txt_unameKeyPressed

