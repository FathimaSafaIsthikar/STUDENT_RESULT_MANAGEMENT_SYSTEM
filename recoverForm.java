    private void btn_recoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_recoverActionPerformed
        String answer=txt_ans.getText();
        if(answer.equals("")){
            JOptionPane.showMessageDialog(null,"Please fill answer to retrieve password");
        }
        else{
            try {
                String sq="SELECT * FROM `user` WHERE `ans`='"+answer+"'";
                ps=conn.prepareStatement(sq);
                rs=ps.executeQuery();
                
                if(rs.next()){
                     String ans = rs.getString(3);
                     txt_pass.setText("Your Password is: "+ans);
                     
                    
                }
                else{
                    JOptionPane.showMessageDialog(null,"Answer is not matched");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());
            }
        }
    }//GEN-LAST:event_btn_recoverActionPerformed

