    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
        String rollNo=txt_rollnum.getText();
        
         try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","");
            Statement st=con.createStatement();
            ResultSet rs= st.executeQuery("select* from student inner join result where student.rollNo='"+rollNo+"' and result.rollNo='"+rollNo+"'");
            
            if(rs.next()){
            
                setVisible(false);
                new studentHome(rollNo).setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Roll No is incorrect");
              
            }
            
        } catch (Exception e) {
              JOptionPane.showMessageDialog(null,"Connection error");
        }
    }//GEN-LAST:event_btn_searchActionPerformed

