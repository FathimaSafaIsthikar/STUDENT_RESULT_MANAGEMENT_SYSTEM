    private void report1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_report1ActionPerformed
        // TODO add your handling code here:
        
        try{
            
            JasperDesign jasdi=JRXmlLoader.load("E:\\Diploma in Software Engineering\\Semester 2 stage 2\\EAD\\Student Result Management System\\src\\stdReport.jrxml");
            String sql="SELECT `rollNo`, `course`, `branch`, `name`, `gender`, `fatherName` FROM `student` ";
            JRDesignQuery newQuery=new JRDesignQuery();
            newQuery.setText(sql);
            jasdi.setQuery(newQuery);
            JasperReport js=JasperCompileManager.compileReport(jasdi);
            JasperPrint jp=JasperFillManager.fillReport(js,null,conn);
            JasperViewer.viewReport(jp);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_report1ActionPerformed

