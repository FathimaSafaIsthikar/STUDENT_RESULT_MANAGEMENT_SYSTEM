    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
         String rollNo=txt_rollnum.getText();
        String phy=txt_phy.getText();
        String che=txt_chem.getText();
        String math=txt_maths.getText();
        
        if(rollNo.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Roll Number field is empty");
        }
        else if(phy.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Physics field is empty");
        }
       
        else if(che.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Chemsitry field is empty");
        }
        else if(math.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Maths field is empty");
        }
        else{

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","");
            Statement st=con.createStatement();
            ResultSet rs= st.executeQuery("select* from student  where rollNo='"+rollNo+"'");
            if(rs.next()){
                st.executeUpdate("update result set phy='"+phy+"',che='"+che+"',math='"+math+"' where rollNo='"+rollNo+"'");
                
                
               DefaultTableModel model=(DefaultTableModel)tbl_view.getModel();
            
            
             if(tbl_view.getSelectedRowCount()==1){
                
                model.setValueAt(rollNo,tbl_view.getSelectedRow(),0);
                model.setValueAt(phy,tbl_view.getSelectedRow(),1);
                model.setValueAt(che,tbl_view.getSelectedRow(),2);
                model.setValueAt(math,tbl_view.getSelectedRow(),3);
                         
                JOptionPane.showMessageDialog(null,"Successfully Updated");
            
            
            }
            else{
                if(tbl_view.getSelectedRowCount()==0){
                     JOptionPane.showMessageDialog(null,"Table is Empty");
                 }
                 else{
                     JOptionPane.showMessageDialog(null,"Please select single row for update..");
                 }
            }
             st.close();
        }
        }
            catch (Exception e) {
              JOptionPane.showMessageDialog(null,"Connection error");
                    setVisible(false);
            new adminHome().setVisible(true);
        }
        
        }
    }//GEN-LAST:event_btn_updateActionPerformed

