    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo=txt_rollnum.getText();

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","");
            Statement st=con.createStatement();
            ResultSet rs= st.executeQuery("select* from student inner join result where student.rollNo='"+rollNo+"' and result.rollNo='"+rollNo+"'");

            while(rs.next()){
                txt_course.setText(rs.getString("course"));
                txt_branch.setText(rs.getString("branch"));
                txt_name.setText(rs.getString("name"));
                txt_gender.setText(rs.getString("gender"));
                txt_fname.setText(rs.getString("fatherName"));
                
                txt_phy.setText(rs.getString("phy"));
                txt_che.setText(rs.getString("che"));
                txt_math.setText(rs.getString("math"));
                
                int s1=Integer.parseInt(txt_phy.getText());
                int s2=Integer.parseInt(txt_che.getText());
                int s3=Integer.parseInt(txt_math.getText());
                
                int result=s1+s2+s3;
                String s=String.valueOf(result);
                txt_tot.setText(s);
                
                if(s1<30||s2<30||s3<30){
                    txt_result.setText("Fail");
                }
                else{
                    txt_result.setText("Pass");
                }
                
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        txt_course.setEditable(false);
        txt_branch.setEditable(false);
        txt_name.setEditable(false);
        txt_gender.setEditable(false);
        txt_fname.setEditable(false);
        
        txt_phy.setEditable(false);
        txt_che.setEditable(false);
        txt_math.setEditable(false);
        txt_tot.setEditable(false);
        txt_result.setEditable(false);
        
        
    }//GEN-LAST:event_formComponentShown

